package com.amrita.jpl.cys21026.pract;
public class PatternProgram {
    public static void main(String[] args) {
        for (int i = 1; i <= 9; i++) {
            if (i % 2 == 0) {
                System.out.println("* * * * * ===================================");
            } else {
                System.out.println("* * * * * * ==================================");
            }
        }
        System.out.println("==============================================");
        System.out.println("==============================================");
        System.out.println("==============================================");
        System.out.println("==============================================");
        System.out.println("==============================================");
        System.out.println("==============================================");
    }
}